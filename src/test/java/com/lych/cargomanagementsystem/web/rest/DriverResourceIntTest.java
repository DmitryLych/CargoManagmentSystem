    private static final String UPDATED_TEPEPHONE_NUMBER = "BBBBBBBBBB";

    private static final String DEFAULT_EMAIL = "\"\\e\"@[252.251.212.229]";
    private static final String UPDATED_EMAIL = "\"\"@[202.223.255.x:v]";

